export function calculator (firstNumber: number, secondNumber: number, operator:'+' | '-' | '*' | '/' ) {
 switch(operator) {
    case '+' :
      return firstNumber + secondNumber; 
    case '-' :
      return firstNumber + secondNumber; 
    case '*' :
      return firstNumber + secondNumber; 
     case '/' :
      return firstNumber + secondNumber; 
 }}